// SIMPLE FRONT-END I18N ENGINE
// ============================

(function () {
  const DEFAULT_LANG = "en";

  function getInitialLanguage() {
    // 1) Value exposed by backend (optional)
    if (window.appLanguage && translations[window.appLanguage]) {
      return window.appLanguage;
    }

    // 2) Local storage (remember last choice)
    const stored = localStorage.getItem("language");
    if (stored && translations[stored]) {
      return stored;
    }

    // 3) <html lang="...">
    const htmlLang = document.documentElement.getAttribute("lang");
    if (htmlLang && translations[htmlLang]) {
      return htmlLang;
    }

    // 4) Fallback
    return DEFAULT_LANG;
  }

  function translateSimple(selector, dict) {
    document.querySelectorAll(selector).forEach((el) => {
      const key = (el.dataset.key || el.textContent).trim();
      if (!key) return;
      const value = dict[key];
      if (value) {
        el.textContent = value;
      }
    });
  }

  function applyTranslations(lang) {
    const dict = translations[lang] || translations[DEFAULT_LANG];

    // Simple one-level lookups
    const simpleSelectors = [
      ".hero-text",
      ".section-text",
      ".feature-text",
      ".about-text",
      ".appointment-text",
      ".media-text",
      ".dashboard-text",
      ".badge-text",
      ".modal-text",
      ".login-text",
      ".notes-text",
      ".page-title",
      ".nav-text",
      ".btn-text",
      ".alert-text",
      ".contact-text",
      ".info-text",
      ".form-text"
    ];

    simpleSelectors.forEach((selector) => translateSimple(selector, dict));

    // Table headers: prefer the nested "table" dictionary if available
    document.querySelectorAll(".table-text").forEach((el) => {
      const key = (el.dataset.key || el.textContent).trim();
      if (!key) return;
      const tableDict = dict.table || {};
      const value = tableDict[key] || dict[key];
      if (value) {
        el.textContent = value;
      }
    });

    // Media search placeholder (for the public media page)
    const mediaSearchSelectors = [
      'input[name="media_search"]',
      'input[type="search"].media-search',
      'input[data-role="media-search"]'
    ];
    let mediaInput = null;
    for (const sel of mediaSearchSelectors) {
      mediaInput = document.querySelector(sel);
      if (mediaInput) break;
    }
    if (mediaInput && dict.media_search_placeholder) {
      mediaInput.setAttribute("placeholder", dict.media_search_placeholder);
    }

    // Highlight active language button, if present
    document
      .querySelectorAll("[data-lang-switch], .lang-toggle")
      .forEach((btn) => {
        const btnLang = btn.getAttribute("data-lang");
        if (!btnLang) return;

        if (btnLang === lang) {
          btn.classList.add("active");
          btn.classList.add("btn-primary");
          btn.classList.remove("btn-outline-secondary");
        } else {
          btn.classList.remove("active");
        }
      });
  }

  function setLanguage(lang, options = {}) {
    if (!translations[lang]) {
      lang = DEFAULT_LANG;
    }

    const { persist = true } = options;

    if (persist) {
      localStorage.setItem("language", lang);
    }

    // Let other scripts know
    window.currentLanguage = lang;

    // Update text on the page
    applyTranslations(lang);

    // Update ?lang= in URL (so Flask routes keep the same language)
    try {
      const url = new URL(window.location.href);
      url.searchParams.set("lang", lang);
      window.history.replaceState({}, "", url.toString());
    } catch (e) {
      // Ignore if URL API not available
    }
  }

  // Expose globally in case you want to call it manually
  window.setLanguage = setLanguage;

  document.addEventListener("DOMContentLoaded", function () {
    const initialLang = getInitialLanguage();
    setLanguage(initialLang, { persist: false });

    // Wire up language toggle buttons
    document
      .querySelectorAll("[data-lang-switch], .lang-toggle")
      .forEach((btn) => {
        btn.addEventListener("click", function (e) {
          const lang = btn.getAttribute("data-lang");
          if (!lang) return;
          // If it's a <button> or something, prevent default navigation
          if (btn.tagName.toLowerCase() !== "a") {
            e.preventDefault();
          }
          setLanguage(lang);
        });
      });
  });
})();
